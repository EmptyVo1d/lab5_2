#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <time.h>
#include "MyLibrary.h"

int main(int argc, char *argv[]) {
    srand(time(NULL));
    int arg = 0;
    int NumEl = 0, NumArr = 0;
    char *PnumEl = malloc(81 * sizeof(char));
    PnumEl[0] = '\0';
    char *PnumArr = malloc(81 * sizeof(char));
    PnumArr[0] = '\0';
    char *alg = malloc(81 * sizeof(char));
    alg[0] = '\0';
    char *field = malloc(81 * sizeof(char));
    field[0] = '\0';
    char *line = malloc(81 * sizeof(char));
    line[0] = '\0';
    if (argc != 10) {
        return 1;
    }
    while ((arg = getopt(argc, argv, "z:x:c:")) && arg != -1) {
        switch (arg) {
            case 'z':
                sscanf(optarg, "%s", alg);
                if ((strcmp(alg, "shaker")) && (strcmp(alg, "pis")) && (strcmp(alg, "qsort"))) {
                    printf("error\n");
                    return 1;
                }
                break;
            case 'x':
                sscanf(optarg, "%s", field);
                if ((strcmp(field, "brand")) && (strcmp(field, "name")) && (strcmp(field, "mileage"))) {
                    return 1;
                }
                break;
            case 'c':
                sscanf(optarg, "%s", line);
                if ((strcmp(line, "up")) && (strcmp(line, "down"))) {
                    return 1;
                }
                break;
            case '?':
                printf("error: %c\n", optopt);
                return 1;
        }
    }
    sscanf(argv[optind], "%s", PnumArr);
    optind++;
    sscanf(argv[optind], "%s", PnumEl);
    NumArr = atoi(PnumArr);
    NumEl = atoi(PnumEl);
    for (int i = 1; i < NumArr + 1; ++i) {
        clock_t start = clock();
        car *arr = malloc(1 * sizeof(car));
        printf("=====array number: %d=====\n", i);
        arr = createArr(NumEl, arr);
        if (strcmp(alg, "qsort") == 0) AlgQsort(field, line, arr, NumEl);
        if (strcmp(alg, "shaker") == 0) AlgShaker(field, line, arr, NumEl);
        if (strcmp(alg, "pis") == 0) PairInsertionSort(field, line, arr, NumEl);
        clock_t end = clock();
        for (int j = 0; j < NumEl; j++)
            printf("=========\nbrand: %s\nname: %s\nmileage: %lf\n", arr[j].brand, arr[j].name, arr[j].mileage);
        printf("program worked: %lfs\n", (double) (end - start) / CLOCKS_PER_SEC);
        for (int j = 0; j < NumEl; j++) {
            free(arr[j].brand);
            free(arr[j].name);
        }
        free(arr);
        start = 0;
        end = 0;
    }
    return 0;
}
